"""## 🔀 Step 5 — Buat Stratified 5-Fold Split

Mengikuti panduan resmi Ultralytics. Setiap gambar dijadikan "feature vector"
berisi jumlah objek per-class, lalu KFold membagi data jadi 5 bagian dengan
distribusi class yang dijaga semirip mungkin antar fold.
"""

import pandas as pd
from sklearn.model_selection import KFold
import datetime

KSPLIT = 5

# Feature vector: baris = gambar, kolom = class, nilai = jumlah objek
index = [lbl.stem for lbl in pool_labels]
labels_df = pd.DataFrame(0.0, index=index, columns=CLS_IDX)

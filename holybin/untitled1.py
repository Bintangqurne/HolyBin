from google.colab import files
import zipfile

print("Upload ZIP dataset dari Roboflow (format YOLOv8):")
uploaded = files.upload()

extract_dir = Path("/content/custom_data")
extract_dir.mkdir(exist_ok=True)
for filename in uploaded:
    if not filename.endswith(".zip"):
        print(f"⚠️  {filename} bukan ZIP, skip.")
        continue
    with zipfile.ZipFile(filename, "r") as z:
        z.extractall(str(extract_dir))
    print(f"✅ Diekstrak: {filename}")

# Gabungkan langsung ke POOL (bukan ke train Roboflow), beri prefix supaya tidak bentrok
add_img = add_lbl = 0
for lbl in extract_dir.rglob("*labels/*.txt"):
    stem = lbl.stem
    img = None
    for ext in (".jpg", ".jpeg", ".png"):
        cand = list(extract_dir.rglob(f"*images/{stem}{ext}"))
        if cand:
            img = cand[0]
            break
    if img is None:
        continue
    shutil.copy2(img, POOL / "images" / f"custom_{img.name}")
    shutil.copy2(lbl, POOL / "labels" / f"custom_{lbl.name}")
    add_img += 1
    add_lbl += 1

print(f"\n✅ Ditambahkan {add_img} gambar ke pool.")
print("🔁 Jalankan ulang dari Step 5 untuk rebuild fold + Step 7 untuk training.")

